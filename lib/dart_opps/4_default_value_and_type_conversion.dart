void main() {
  //string to int
  int a = 10;
  int b = int.parse("20");

  print(a + b);

  //string to double
  int c = 10;
  double d = double.parse("2.2");

  print(c + d);

  //int to string
  String x = 50.toString();
  print(x);

  // double to string
  String z= 45.50.toString();
  print(z);

}
